CREATE TABLE EMPLOYEE
(
    Empno VARCHAR(5) PRIMARY KEY,
    Ename VARCHAR(20) UNIQUE,
    Designation VARCHAR(20) NOT NULL,
    Salary FLOAT(10) DEFAULT 25000,
    DOB DATE NOT NULL,
    Dno FLOAT(3),
    CONSTRAINT FOREIGN KEY (Dno) REFERENCES DEPARTMENT(Deptno),
    CONSTRAINT CHECK (Salary BETWEEN 15000 AND 50000),
    CONSTRAINT CHECK (Empno LIKE "E%")
);