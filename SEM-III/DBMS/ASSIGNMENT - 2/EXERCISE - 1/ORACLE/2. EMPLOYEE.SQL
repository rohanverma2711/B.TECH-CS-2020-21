CREATE TABLE EMPLOYEE
(
    Empno VARCHAR(5) PRIMARY KEY,
    Ename VARCHAR(20) UNIQUE,
    Designation VARCHAR(20) NOT NULL,
    Salary NUMBER(10) DEFAULT 25000,
    DOB DATE NOT NULL,
    Dno NUMBER(3),
    CONSTRAINT Dno FOREIGN KEY (Dno) REFERENCES DEPARTMENT (Deptno), 
    CONSTRAINT Salary CHECK (Salary BETWEEN 15000 AND 50000),
    CONSTRAINT Empno CHECK (Empno LIKE 'E%')
);
